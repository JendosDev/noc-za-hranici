package com.nocZaHranici;

public class Quest {
    private String name;
    private String status; 

    public Quest(String name, String status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
